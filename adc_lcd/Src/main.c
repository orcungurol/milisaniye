long map(long x, long in_min, long in_max, long out_min, long out_max)
{
return (x - in_min) * (out_max - out_min) / (in_max - in_min) + out_min;

}
int main()
{
  HAL_Init();
	lcd_init();

  SystemClock_Config();



  MX_GPIO_Init();
  MX_ADC1_Init();
  MX_I2C1_Init();

	lcd_clear();
	lcd_home();
	lcd_send_string ("Milisaniye");
	lcd_shiftDecrement();
	lcd_send_string("Pot deger=");

  while (1)
  {
    /* USER CODE END WHILE */
		HAL_ADC_Start(&hadc1);
		HAL_ADC_PollForConversion(&hadc1 , 5000);
		uint32_t deger = HAL_ADC_GetValue(&hadc1);
		deger=map(deger,0,4000,0,255);
		sprintf(pot_deger,"%i", deger);
		if(deger < 100)
		{
			lcd_gotoxy(12,1);
			lcd_send_string(" ");
			if(deger <10){
				lcd_gotoxy(11,1);
				lcd_send_string(" ");
			}
		}
		lcd_gotoxy(10,1);
		lcd_send_string(pot_deger);
		HAL_ADC_Stop(&hadc1);
		lcd_send_cmd(0xc0);
		HAL_Delay(300);
  
  }

}


